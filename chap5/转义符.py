print('hello\nworld')

# \n 换行符   new  line

#   \t  table  四个空格  制表符
print('hello\tworld')

print('helloooo\tworld')  #\t 四个字符位  4个一组

print('hello\rworld')   #覆盖前面的字符串

print('hello\bworld')  #退一个字符

print('http:\\\\www.baidu.com')   #  \\  只有一个\   所以要用俩

print('老师说：\'大家好\'')   # 转义符让‘能显示

print(r'hello\nworld')   #  引号前面加一个r或者R  能讲将转义符输出  但最后一个字符不能是一个反斜线\   两个可以



